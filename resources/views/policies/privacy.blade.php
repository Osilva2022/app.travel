                    <h2>
                        ¿Se hace uso de cookies y web beacons para obtener datos personales?
                    </h2>
                    <p>
                        Las cookies son archivos de texto que son descargados automáticamente y almacenados en el disco duro
                        del equipo de cómputo del usuario al navegar en una página de Internet específica, que permiten
                        recordar al servidor de Internet algunos datos sobre este usuario, entre ellos, sus preferencias
                        para la visualización de las páginas en ese servidor, nombre y contraseña. Por su parte, las web
                        beacons son imágenes insertadas en una página de Internet o correo electrónico, que puede ser
                        utilizado para monitorear el comportamiento de un visitante, como almacenar información sobre la
                        dirección IP del usuario, duración del tiempo de interacción en dicha página y el tipo de navegador
                        utilizado, entre otros. Estas cookies y otras tecnologías pueden ser deshabilitadas directamente por
                        el usuario a través de su propio navegador de internet, sin que para ello se requiera asistencia o
                        apoyo por parte de Compañía Periodística Sudcaliforniana S.A. de C.V.
                    </p>
